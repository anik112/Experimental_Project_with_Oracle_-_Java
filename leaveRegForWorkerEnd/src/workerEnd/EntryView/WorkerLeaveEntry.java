    private void showDataInComboCompany() {
        LeaveEntryCore leaveEntryCore = new LeaveEntryCore();
        List<String> companyName = leaveEntryCore.getCompany();

        for (int i = 0; i < companyName.size(); i++) {
            comboCompanyName.addItem(companyName.get(i));
        }
    }

    private void showDataInComboSupervisorCardno() {

        LeaveEntryCore leaveEntryCore = new LeaveEntryCore();
        ResultSet rs = leaveEntryCore.getSuperVisor(textWorkerCardno.getText(), comboCompanyName.getSelectedItem().toString());

        try {

            while (rs.next()) {
                comboSupervisorCardno.addItem(rs.getString(1) + ":" + rs.getString(2));
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

