    private void textStartDateMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textStartDateMouseReleased
        // TODO add your handling code here:
        if (textStartDate.getText() != null) {

            String fullDate = textStartDate.getText();
            if (fullDate.length() == 8) {
                String day = fullDate.substring(0, 2);
                String month = fullDate.substring(2, 4);
                String year = fullDate.substring(4, 8);

                finStrDate = "" + month + "/" + day + "/" + year;
                textStartDate.setText(finStrDate);
            }
        }

    }//GEN-LAST:event_textStartDateMouseReleased

