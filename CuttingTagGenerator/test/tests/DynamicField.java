    private void btnAddRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRowActionPerformed
        // TODO add your handling code here:
        combo1_combo2_text1_array(Integer.valueOf(txtRollSize.getText()));
        if(count == max_row-1){
            JOptionPane.showMessageDialog(null, "Maximum of 10 rows can be added","Failed!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        count++;        
        combo1[count] = new javax.swing.JComboBox(); 
        for(int i=1;i<=5;i++){
            combo1[count].addItem("Item " + i);
        }       
        combo2[count] = new javax.swing.JComboBox(); 
        for(int i=1;i<=5;i++){
            combo2[count].addItem("Item " + i);
        }   
        text1[count] = new javax.swing.JTextField();
        text1[count].setSize(50, 50);
        combo1[count].setSize(50, 50);
        combo2[count].setSize(50, 50);
        mainPan.setLayout(new GridLayout(0,3,20,20));
        mainPan.add(combo1[count]);
        mainPan.add(combo2[count]);
        mainPan.add(text1[count]);
        mainPan.revalidate();
        mainPan.repaint();

    }//GEN-LAST:event_btnAddRowActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if(count > -1){ // Deleting one row at a time
            mainPan.remove(combo1[count]);
            mainPan.remove(combo2[count]);
            mainPan.remove(text1[count]);
            count--;
            mainPan.revalidate();
            mainPan.repaint();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

