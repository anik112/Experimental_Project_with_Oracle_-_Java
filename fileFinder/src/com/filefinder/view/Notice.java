    public void showNotice(String fileWorkingPath) {

        File workingFile = new File(fileWorkingPath);
        String msg = "";
        try {
            Scanner scanner = new Scanner(workingFile);
            while (scanner.hasNextLine()) {
                msg += scanner.nextLine();
                msg += "\n";
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Notice.class.getName()).log(Level.SEVERE, null, ex);
        }
        
//        System.out.println(msg);
        txtAriaShowNotice.setText(txtAriaShowNotice.getText()+"\n"+msg);
    }

