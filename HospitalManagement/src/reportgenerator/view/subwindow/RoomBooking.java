    public void showDataListInTable() {

        roomInfo = new RoombookingService().getDataFromPatientTable();

        DefaultTableModel model = (DefaultTableModel) tblShowAdvenceAmountData.getModel();
        model.setRowCount(0);

        Object row[] = new Object[2];

        for (int i = 0; i < roomInfo.size(); i++) {
            row[0] = roomInfo.get(i).getPatientId();
            row[1] = roomInfo.get(i).getDoctorId();
            model.addRow(row);
        }
    }


