     private void displayInformationWithInTable() {

        admissionService = new PatientAdmissionService();

        List<PatientAdmissionInfo> getinformation = admissionService.searchData(Integer.parseInt(patient_id.getText()));
        DefaultTableModel model = (DefaultTableModel) tblDisplayBookInfo.getModel();
        Object[] row = new Object[10];


        for (int i = 0; i < getinformation.size(); i++) {

            row[0] = getinformation.get(i).getAdmission_id();
            row[1] = getinformation.get(i).getPatient_id();
            row[2] = getinformation.get(i).getGuardian_name();
            row[3] = getinformation.get(i).getGuardian_phone();
            row[4] = getinformation.get(i).getRoom_no();
            row[5] = getinformation.get(i).getBad_no();
            row[6] = getinformation.get(i).getRef_doctor();
            row[7] = getinformation.get(i).getAdmission_date();
            row[8] = getinformation.get(i).getAdmission_time();
            row[9] = getinformation.get(i).getEmergency();
            
            model.addRow(row);
        }
    }

    private void clearTableForBookInformation() {

        DefaultTableModel model = (DefaultTableModel) tblDisplayBookInfo.getModel();
        model.setRowCount(0);

        displayInformationWithInTable();

    }
    
