    public void showDataListInTable() {

        patientInfo = new PatientService().getDataFromPatientTable();

        DefaultTableModel model = (DefaultTableModel) tblShowPatientInfo.getModel();
        model.setRowCount(0);

        Object row[] = new Object[6];

        for (int i = 0; i < patientInfo.size(); i++) {
            row[0] = patientInfo.get(i).getId();
            row[1] = patientInfo.get(i).getName();
            row[2] = patientInfo.get(i).getPatient_sex();
            row[3] = patientInfo.get(i).getPatient_hphone();
            row[4] = patientInfo.get(i).getPatient_blood_group();
            row[5] = patientInfo.get(i).getPatient_dob();

            model.addRow(row);
        }
    }


