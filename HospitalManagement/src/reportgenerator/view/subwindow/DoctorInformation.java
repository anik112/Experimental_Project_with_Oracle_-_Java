    public void showDataListInTable() {

        doctorInfo = new DoctorService().getDataFromDoctorTable();

        DefaultTableModel model = (DefaultTableModel) tblShowAdvenceAmountData.getModel();
        model.setRowCount(0);

        Object row[] = new Object[6];

        for (int i = 0; i < doctorInfo.size(); i++) {
            row[0] = doctorInfo.get(i).getId();
            row[1] = doctorInfo.get(i).getName();
            row[2] = doctorInfo.get(i).getGender();
            row[3] = doctorInfo.get(i).getPhone();
            row[4] = doctorInfo.get(i).getQualfication();
            row[5] = doctorInfo.get(i).getSpecialization();

            model.addRow(row);
        }
    }


