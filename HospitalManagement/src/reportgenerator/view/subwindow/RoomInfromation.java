    public void showDataListInTable() {

        info = new RoomInfoService().getDataFromPatientTable();

        DefaultTableModel model = (DefaultTableModel) tblShowAdvenceAmountData.getModel();
        model.setRowCount(0);

        Object row[] = new Object[3];

        for (int i = 0; i < info.size(); i++) {
            row[0] = info.get(i).getId();
            row[1] = info.get(i).getRoom_id();
            row[2] = info.get(i).getRoom_type();
            
            model.addRow(row);
        }
    }


