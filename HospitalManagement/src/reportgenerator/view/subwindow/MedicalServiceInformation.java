    public void showDataListInTable() {

        medicalService = new MedicalService().getDataFromDoctorTable();

        DefaultTableModel model = (DefaultTableModel) tblShowPatientInfo.getModel();
        model.setRowCount(0);
        
        Object row[] = new Object[4];
        
        for (int i = 0; i < medicalService.size(); i++) {
            row[0] = medicalService.get(i).getId();
            row[1] = medicalService.get(i).getChannel_service();
            row[2] = medicalService.get(i).getDuration_of_service();
            row[3] = medicalService.get(i).getCharge_for_service();
            
            model.addRow(row);
        }
    }


