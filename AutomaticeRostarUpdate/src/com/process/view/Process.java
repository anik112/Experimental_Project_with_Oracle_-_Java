    // Show list of data
    private void runFrem(List<String> list) {
        jf.setSize(262, 394);
        jf.setLocation(150, 100);
        jf.setTitle("Find...");
        jf.setResizable(false);
        jf.add(jPanSearch);
        txtFind.setText("%");

        DefaultTableModel model = (DefaultTableModel) tblShowData.getModel();
        model.setRowCount(0);
        Object row[] = new Object[1];
        ProcessHelper ph = new ProcessHelper();

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i);
            model.addRow(row);
        }
        jf.setVisible(true);
    }

