    private void tblShowDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblShowDataMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tblShowData.getModel();
        int id = tblShowData.getSelectedRow();

        returnValue = dtm.getValueAt(id, NORMAL).toString();
        System.out.println(returnValue);

        switch (check) {
            case COMPANY_CHECK:
                txtCompanyName.setText(returnValue);
                break;
            case DPT_CHECK:
                txtDepartment.setText(returnValue);
                break;
            case SEC_CHECK:
                txtSectionNm.setText(returnValue);
                break;
            case CARDNO_CHECK:
                String[] str=returnValue.split(",");
                txtCardno.setText(str[0]);
                break;
            case LINE_NO_CHECK:
                txtLineNo.setText(returnValue);
                break;
            default:
                break;
        }
        jf.hide();
    }//GEN-LAST:event_tblShowDataMouseClicked

